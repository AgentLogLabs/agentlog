// 核心方法：生成 Commit 解释摘要
// ─────────────────────────────────────────────

/**
 * 生成指定 Commit 的 AI 交互解释摘要。
 *
 * 输出为结构化数据 + 渲染后的 Markdown 文本，概括每条 AI 会话的用户意图和 AI 回应。
 *
 * @param commitHash    Git Commit Hash
 * @param workspacePath 工作区路径（可选）
 * @param language      输出语言，默认 'zh'
 */
export async function generateCommitExplain(
  commitHash: string,
  workspacePath?: string,
  language: ExportLanguage = "zh",
): Promise<CommitExplainResult> {
  const data = await resolveCommitData(commitHash, workspacePath);

  // 构建 SessionExplainItems
  const explainItems = data.sessions.map(sessionToExplainItem);

  // 聚合文件
  const allAffectedFiles = aggregateFiles(data.sessions);

  // 生成总体摘要
  const overallSummary = buildOverallSummary(
    data.sessions,
    allAffectedFiles,
    language,
  );

  // 渲染 Markdown
  const content = renderExplainMarkdown(
    data.resolvedHash,
    data.commitMessage,
    data.authorName,
    data.committedAt,
    overallSummary,
    explainItems,
    allAffectedFiles,
    language,
  );

  return {
    commitHash: data.resolvedHash,
    commitMessage: data.commitMessage,
    authorName: data.authorName,
    committedAt: data.committedAt,
    overallSummary,
    sessions: explainItems,
    allAffectedFiles,
    language,
    content,
    generatedAt: new Date().toISOString(),
  };
}

// ─────────────────────────────────────────────
// 总体摘要构建
// ─────────────────────────────────────────────

function buildOverallSummary(
  sessions: AgentSession[],
  allAffectedFiles: string[],
  language: ExportLanguage,
): string {
  const t = explainI18n[language];

  if (sessions.length === 0) {
    return t.summaryNoSession;
  }

  const uniqueModels = [...new Set(sessions.map((s) => s.model))];

  return t.summaryTemplate(
    sessions.length,
    uniqueModels,
    allAffectedFiles.length,
  );
}

// ─────────────────────────────────────────────
// 渲染：Markdown 上下文
// ─────────────────────────────────────────────

function renderContextMarkdown(
  commitHash: string,
  commitMessage: string,
  authorName: string,
  committedAt: string,
  changedFiles: string[],
  sessions: ContextSessionItem[],
  opts: Required<CommitContextOptions>,
): string {
  const t = contextI18n[opts.language];
  const lines: string[] = [];

  // 标题
  lines.push(`# ${t.title}`);
  lines.push("");

  // Commit 信息
  lines.push(t.commitInfo);
  lines.push("");
  lines.push(
    `| ${t.hash} | ${t.message} | ${t.author} | ${t.time} | ${t.totalSessions} |`,
  );
  lines.push("|:---|:---|:---|:---|:---|");
  lines.push(
    `| \`${commitHash.slice(0, 8)}\` | ${commitMessage || "-"} | ${authorName || "-"} | ${committedAt ? formatTime(committedAt) : "-"} | ${sessions.length} |`,
  );
  lines.push("");

  // 变更文件
  if (opts.includeChangedFiles && changedFiles.length > 0) {
    lines.push(t.changedFiles);
    lines.push("");
    for (const f of changedFiles) {
      lines.push(`- \`${f}\``);
    }
    lines.push("");
  }

  // AI 交互记录
  lines.push(t.aiSessions);
  lines.push("");

  if (sessions.length === 0) {
    lines.push(t.noSessions);
    lines.push("");
  } else {
    sessions.forEach((item, idx) => {
      renderContextSessionMarkdown(item, idx, t, lines);
    });
  }

  // 使用提示
  lines.push(t.usageHint);
  lines.push("");
  lines.push(t.usageHintContent);
  lines.push("");

  // 页脚
  lines.push("---");
  lines.push(
    `<sub>${t.generatedBy} | ${t.generatedAt}: ${formatTime(new Date().toISOString())}</sub>`,
  );

  return lines.join("\n");
}

/** 渲染单条会话的 Markdown 块 */
function renderContextSessionMarkdown(
  item: ContextSessionItem,
  idx: number,
  t: (typeof contextI18n)[ExportLanguage],
  lines: string[],
): void {
  const shortId = item.sessionId.slice(0, 8);

  lines.push(`${t.sessionIndex} #${idx + 1} <sub>\`${shortId}\`</sub>`);
  lines.push("");

  // 元数据表格
  lines.push(
    `| ${t.model} | ${t.source} | ${t.provider} | ${t.time} | ${t.duration} |`,
  );
  lines.push("|:---|:---|:---|:---|:---|");
  lines.push(
    `| \`${item.model}\` | \`${item.source}\` | \`${item.provider}\` | ${formatTime(item.createdAt)} | ${item.durationMs}${t.ms} |`,
  );
  lines.push("");

  // Prompt
  if (item.prompt) {
    lines.push(`**${t.prompt}**`);
    lines.push("");
    lines.push(`> ${item.prompt.replace(/\n/g, "\n> ")}`);
    lines.push("");
  }

  // Reasoning
  if (item.reasoning) {
    lines.push(`**${t.reasoning}**`);
    lines.push("");
    lines.push("<details>");
    lines.push(`<summary>${t.reasoning}</summary>`);
    lines.push("");
    lines.push(item.reasoning);
    lines.push("");
    lines.push("</details>");
    lines.push("");
  }

  // Response
  if (item.response) {
    lines.push(`**${t.response}**`);
    lines.push("");
    lines.push(`> ${item.response.replace(/\n/g, "\n> ")}`);
    lines.push("");
  }

  // 涉及文件
  if (item.affectedFiles.length > 0) {
    lines.push(
      `**${t.affectedFiles}**：${item.affectedFiles.map((f) => `\`${f}\``).join(" · ")}`,
    );
    lines.push("");
  }

  // 标签
  if (item.tags && item.tags.length > 0) {
    lines.push(
      `**${t.tags}**：${item.tags.map((tag) => `\`${tag}\``).join(" ")}`,
    );
    lines.push("");
  }

  // 备注
  if (item.note) {
    lines.push(`**${t.note}**：${item.note}`);
    lines.push("");
  }

  lines.push("---");
  lines.push("");
}

// ─────────────────────────────────────────────
// 渲染：JSON 上下文
// ─────────────────────────────────────────────

function renderContextJson(
  commitHash: string,
  commitMessage: string,
  authorName: string,
  committedAt: string,
  changedFiles: string[],
  sessions: ContextSessionItem[],
  opts: Required<CommitContextOptions>,
): string {
  const doc = {
    _type: "agentlog-commit-context",
    _version: "1.0",
    commit: {
      hash: commitHash,
      shortHash: commitHash.slice(0, 8),
      message: commitMessage,
      author: authorName,
      committedAt,
      changedFiles: opts.includeChangedFiles ? changedFiles : undefined,
    },
    sessionCount: sessions.length,
    sessions: sessions.map((item) => ({
      sessionId: item.sessionId,
      createdAt: item.createdAt,
      model: item.model,
      provider: item.provider,
      source: item.source,
      durationMs: item.durationMs,
      prompt: item.prompt,
      reasoning: item.reasoning,
      response: item.response,
      affectedFiles:
        item.affectedFiles.length > 0 ? item.affectedFiles : undefined,
      tags: item.tags && item.tags.length > 0 ? item.tags : undefined,
      note: item.note || undefined,
    })),
    generatedAt: new Date().toISOString(),
    generatedBy: "AgentLog",
  };

  return JSON.stringify(doc, null, 2);
}

// ─────────────────────────────────────────────
// 渲染：XML 上下文
// ─────────────────────────────────────────────

function renderContextXml(
  commitHash: string,
  commitMessage: string,
  authorName: string,
  committedAt: string,
  changedFiles: string[],
  sessions: ContextSessionItem[],
  opts: Required<CommitContextOptions>,
): string {
  const lines: string[] = [];

  lines.push('<?xml version="1.0" encoding="UTF-8"?>');
  lines.push('<agentlog-commit-context version="1.0" generatedBy="AgentLog">');

  // Commit 信息
  lines.push("  <commit>");
  lines.push(`    <hash>${xmlEscape(commitHash)}</hash>`);
  lines.push(`    <shortHash>${xmlEscape(commitHash.slice(0, 8))}</shortHash>`);
  lines.push(`    <message>${xmlEscape(commitMessage)}</message>`);
  lines.push(`    <author>${xmlEscape(authorName)}</author>`);
  lines.push(`    <committedAt>${xmlEscape(committedAt)}</committedAt>`);

  if (opts.includeChangedFiles && changedFiles.length > 0) {
    lines.push("    <changedFiles>");
    for (const f of changedFiles) {
      lines.push(`      <file>${xmlEscape(f)}</file>`);
    }
    lines.push("    </changedFiles>");
  }
  lines.push("  </commit>");

  // 会话
  lines.push(`  <sessions count="${sessions.length}">`);
  for (const item of sessions) {
    lines.push(`    <session id="${xmlEscape(item.sessionId)}">`);
    lines.push(`      <createdAt>${xmlEscape(item.createdAt)}</createdAt>`);
    lines.push(`      <model>${xmlEscape(item.model)}</model>`);
    lines.push(`      <provider>${xmlEscape(item.provider)}</provider>`);
    lines.push(`      <source>${xmlEscape(item.source)}</source>`);
    lines.push(`      <durationMs>${item.durationMs}</durationMs>`);

    if (item.prompt) {
      lines.push(`      <prompt><![CDATA[${item.prompt}]]></prompt>`);
    }
    if (item.reasoning) {
      lines.push(`      <reasoning><![CDATA[${item.reasoning}]]></reasoning>`);
    }
    if (item.response) {
      lines.push(`      <response><![CDATA[${item.response}]]></response>`);
    }

    if (item.affectedFiles.length > 0) {
      lines.push("      <affectedFiles>");
      for (const f of item.affectedFiles) {
        lines.push(`        <file>${xmlEscape(f)}</file>`);
      }
      lines.push("      </affectedFiles>");
    }

    if (item.tags && item.tags.length > 0) {
      lines.push("      <tags>");
      for (const tag of item.tags) {
        lines.push(`        <tag>${xmlEscape(tag)}</tag>`);
      }
      lines.push("      </tags>");
    }

    if (item.note) {
      lines.push(`      <note>${xmlEscape(item.note)}</note>`);
    }

    lines.push("    </session>");
  }
  lines.push("  </sessions>");

  lines.push(`  <generatedAt>${new Date().toISOString()}</generatedAt>`);
  lines.push("</agentlog-commit-context>");

  return lines.join("\n");
}

// ─────────────────────────────────────────────
// 渲染：Markdown 解释摘要
// ─────────────────────────────────────────────

function renderExplainMarkdown(
  commitHash: string,
  commitMessage: string,
  authorName: string,
  committedAt: string,
  overallSummary: string,
  sessions: SessionExplainItem[],
  allAffectedFiles: string[],
  language: ExportLanguage,
): string {
  const t = explainI18n[language];
  const lines: string[] = [];

  // 标题
  lines.push(`# ${t.title}`);
  lines.push("");

  // Commit 信息
  lines.push(t.commitInfo);
  lines.push("");
  lines.push(`| ${t.hash} | ${t.message} | ${t.author} | ${t.time} |`);
  lines.push("|:---|:---|:---|:---|");
  lines.push(
    `| \`${commitHash.slice(0, 8)}\` | ${commitMessage || "-"} | ${authorName || "-"} | ${committedAt ? formatTime(committedAt) : "-"} |`,
  );
  lines.push("");

  // 总体概述
  lines.push(t.overallSummary);
  lines.push("");
  lines.push(overallSummary);
  lines.push("");

  // 会话详情
  if (sessions.length > 0) {
    lines.push(t.sessionDetails);
    lines.push("");

    sessions.forEach((item, idx) => {
      const shortId = item.sessionId.slice(0, 8);
      lines.push(`${t.sessionIndex} ${idx + 1}. <sub>\`${shortId}\`</sub>`);
      lines.push("");

      // 元数据表格
      lines.push(
        `| ${t.model} | ${t.source} | ${t.timeLabel} | ${t.hasReasoning} |`,
      );
      lines.push("|:---|:---|:---|:---|");
      lines.push(
        `| \`${item.model}\` | \`${item.source}\` | ${formatTime(item.createdAt)} | ${item.hasReasoning ? t.yes : t.no} |`,
      );
      lines.push("");

      // 用户意图
      lines.push(`**${t.promptSummary}**：${item.promptSummary || "-"}`);
      lines.push("");

      // AI 回应
      lines.push(`**${t.responseSummary}**：${item.responseSummary || "-"}`);
      lines.push("");

      // 涉及文件
      if (item.affectedFiles.length > 0) {
        lines.push(
          `**${t.affectedFiles}**：${item.affectedFiles.map((f) => `\`${f}\``).join(" · ")}`,
        );
        lines.push("");
      }

      // 标签
      if (item.tags && item.tags.length > 0) {
        lines.push(
          `**${t.tags}**：${item.tags.map((tag) => `\`${tag}\``).join(" ")}`,
        );
        lines.push("");
      }

      lines.push("---");
      lines.push("");
    });
  } else {
    lines.push(`> ${t.noSessions}`);
    lines.push("");
  }

  // 涉及文件汇总
  if (allAffectedFiles.length > 0) {
    lines.push(t.allAffectedFiles);
    lines.push("");
    for (const f of allAffectedFiles) {
      lines.push(`- \`${f}\``);
    }
    lines.push("");
  }

  // 页脚
  lines.push("---");
  lines.push(
    `<sub>${t.generatedBy} | ${t.generatedAt}: ${formatTime(new Date().toISOString())}</sub>`,
  );

  return lines.join("\n");
}
