/**
 * 生成指定 Commit 的上下文文档。
 *
 * 输出格式支持 Markdown / JSON / XML，可直接粘贴到新 AI 对话中作为上下文。
 *
 * @param commitHash    Git Commit Hash（完整或短 hash）
 * @param workspacePath 工作区路径（可选，用于从 git 获取实时 commit 信息）
 * @param options       上下文选项
 */
export async function generateCommitContext(
  commitHash: string,
  workspacePath?: string,
  options: CommitContextOptions = {},
): Promise<CommitContextResult> {
  const opts: Required<CommitContextOptions> = {
    ...DEFAULT_OPTIONS,
    ...options,
  };

  const data = await resolveCommitData(commitHash, workspacePath);

  // 应用 maxSessions 限制
  let { sessions } = data;
  if (opts.maxSessions > 0 && sessions.length > opts.maxSessions) {
    sessions = sessions.slice(0, opts.maxSessions);
  }

  // 构建 ContextSessionItems
  const contextItems = sessions.map((s) => sessionToContextItem(s, opts));

  // 渲染输出
  let content: string;
  switch (opts.format) {
    case "markdown":
      content = renderContextMarkdown(
        data.resolvedHash,
        data.commitMessage,
        data.authorName,
        data.committedAt,
        data.changedFiles,
        contextItems,
        opts,
      );
      break;
    case "json":
      content = renderContextJson(
        data.resolvedHash,
        data.commitMessage,
        data.authorName,
        data.committedAt,
        data.changedFiles,
        contextItems,
        opts,
      );
      break;
    case "xml":
      content = renderContextXml(
        data.resolvedHash,
        data.commitMessage,
        data.authorName,
        data.committedAt,
        data.changedFiles,
        contextItems,
        opts,
      );
      break;
    default:
      throw new Error(
        `[contextService] 不支持的上下文格式：${opts.format as string}`,
      );
  }

  return {
    commitHash: data.resolvedHash,
    commitMessage: data.commitMessage,
    authorName: data.authorName,
    committedAt: data.committedAt,
    changedFiles: opts.includeChangedFiles ? data.changedFiles : [],
    format: opts.format,
    sessionCount: contextItems.length,
    content,
    generatedAt: new Date().toISOString(),
  };
}

