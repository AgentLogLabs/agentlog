/** 截断文本到指定长度 */
function truncate(
  text: string,
  maxLength: number,
  suffix = "…(truncated)",
): string {
  if (maxLength <= 0 || text.length <= maxLength) return text;
  return text.slice(0, maxLength) + suffix;
}

/** 格式化时间为可读字符串 */
function formatTime(isoStr: string): string {
  try {
    const d = new Date(isoStr);
    const pad = (n: number) => String(n).padStart(2, "0");
    return `${d.getFullYear()}-${pad(d.getMonth() + 1)}-${pad(d.getDate())} ${pad(d.getHours())}:${pad(d.getMinutes())}:${pad(d.getSeconds())}`;
  } catch {
    return isoStr;
  }
}

/** 提取文本的第一行作为摘要（最多 maxLen 字符） */
function extractSummary(text: string, maxLen = 120): string {
  if (!text) return "";
  const firstLine = text.split("\n")[0].trim();
  if (firstLine.length <= maxLen) return firstLine;
  return firstLine.slice(0, maxLen) + "…";
}

/** 聚合所有会话涉及的文件（去重排序） */
function aggregateFiles(sessions: AgentSession[]): string[] {
  const set = new Set<string>();
  for (const s of sessions) {
    for (const f of s.affectedFiles) {
      set.add(f);
    }
  }
  return [...set].sort();
}

/** 从数据库获取 CommitBinding 行（支持短 hash 前缀匹配） */
function getCommitBindingRow(commitHash: string): CommitRow | null {
  const db = getDatabase();
  const row =
    commitHash.length >= 40
      ? (db
          .prepare("SELECT * FROM commit_bindings WHERE commit_hash = ?")
          .get(commitHash) as CommitRow | undefined)
      : (db
          .prepare("SELECT * FROM commit_bindings WHERE commit_hash LIKE ?")
          .get(`${commitHash}%`) as CommitRow | undefined);
  return row ?? null;
}

/** XML 转义 */
function xmlEscape(str: string): string {
  return str
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&apos;");
}

/** 将 AgentSession 映射为 ContextSessionItem */
function sessionToContextItem(
  session: AgentSession,
  opts: Required<CommitContextOptions>,
): ContextSessionItem {
  return {
    sessionId: session.id,
    createdAt: session.createdAt,
    model: session.model,
    provider: session.provider,
    source: session.source,
    prompt: opts.includePrompts
      ? truncate(session.prompt, opts.maxContentLength)
      : undefined,
    reasoning:
      opts.includeReasoning && session.reasoning
        ? truncate(session.reasoning, opts.maxContentLength)
        : undefined,
    response: opts.includeResponses
      ? truncate(session.response, opts.maxContentLength)
      : undefined,
    affectedFiles: session.affectedFiles,
    tags: session.tags,
    note: session.note,
    durationMs: session.durationMs,
  };
}

/** 将 AgentSession 映射为 SessionExplainItem */
function sessionToExplainItem(session: AgentSession): SessionExplainItem {
  return {
    sessionId: session.id,
    createdAt: session.createdAt,
    model: session.model,
    source: session.source,
    promptSummary: extractSummary(session.prompt),
    responseSummary: extractSummary(session.response),
    hasReasoning: !!session.reasoning,
    affectedFiles: session.affectedFiles,
    tags: session.tags,
  };
}

// ─────────────────────────────────────────────
// 内部：解析 Commit 信息 + 会话
// ─────────────────────────────────────────────

interface ResolvedCommitData {
  resolvedHash: string;
  commitMessage: string;
  authorName: string;
  committedAt: string;
  changedFiles: string[];
  sessions: AgentSession[];
}

/**
 * 统一的 commit 数据解析逻辑，供 context / explain 共用。
 *
 * 优先从 git 获取实时信息，回退到数据库 commit_bindings 表。
 * 会话优先通过 agent_sessions.commit_hash 查询，回退到 commit_bindings.session_ids。
 */
async function resolveCommitData(
  commitHash: string,
  workspacePath?: string,
): Promise<ResolvedCommitData> {
  let commitMessage = "";
  let authorName = "";
  let committedAt = "";
  let changedFiles: string[] = [];
  let resolvedHash = commitHash;

  // 1. 优先从 git 获取 commit 元信息
  if (workspacePath) {
    try {
      const info = await getCommitInfo(workspacePath, commitHash);
      resolvedHash = info.hash;
      commitMessage = info.message;
      authorName = info.authorName;
      committedAt = info.committedAt;
      changedFiles = info.changedFiles;
    } catch {
      // git 获取失败（可能 hash 不在本仓库、或目录不是 git 仓库），回退到数据库
    }
  }

  // 2. 如果 git 未获取到，尝试从 commit_bindings 表
  if (!commitMessage) {
    const bindingRow = getCommitBindingRow(commitHash);
    if (bindingRow) {
      resolvedHash = bindingRow.commit_hash;
      commitMessage = bindingRow.message;
      authorName = bindingRow.author_name;
      committedAt = bindingRow.committed_at;
      changedFiles = fromJson<string[]>(bindingRow.changed_files, []);
    }
  }

  // 3. 获取关联的 AI 会话
  //    优先通过 agent_sessions 表的 commit_hash 字段查询
  let sessions = getSessionsByCommitHash(resolvedHash);

  //    若无结果，尝试通过 commit_bindings 表的 session_ids 逐条查找
  if (sessions.length === 0) {
    const bindingRow = getCommitBindingRow(resolvedHash);
    if (bindingRow) {
      const sessionIds = fromJson<string[]>(bindingRow.session_ids, []);
      sessions = sessionIds
        .map((id) => getSessionById(id))
        .filter((s): s is AgentSession => s !== null);
    }
  }

  return {
    resolvedHash,
    commitMessage,
    authorName,
    committedAt,
    changedFiles,
    sessions,
  };
}

