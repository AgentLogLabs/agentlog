/** 截断字符串，超长时追加省略号 */
function truncate(str: string, maxLen: number): string {
  if (str.length <= maxLen) return str;
  return str.slice(0, maxLen) + '…';
}

/** 格式化 ISO 时间为人类可读字符串 */
function formatTime(iso: string): string {
  try {
    const d = new Date(iso);
    const pad = (n: number) => String(n).padStart(2, '0');
    return `${d.getFullYear()}-${pad(d.getMonth() + 1)}-${pad(d.getDate())} ${pad(d.getHours())}:${pad(d.getMinutes())}`;
  } catch {
    return iso;
  }
}

/** 将 ISO 日期截取为 YYYY-MM-DD */
function toDateStr(iso: string): string {
  return iso.slice(0, 10);
}

/** 按日期（YYYY-MM-DD）分组会话，返回有序 Map */
function groupSessionsByDate(
  sessions: AgentSession[],
): Map<string, AgentSession[]> {
  const map = new Map<string, AgentSession[]>();

  // 按时间正序排列，使同一天的记录按时间顺序展示
  const sorted = [...sessions].sort(
    (a, b) => new Date(a.createdAt).getTime() - new Date(b.createdAt).getTime(),
  );

  for (const session of sorted) {
    const date = toDateStr(session.createdAt);
    const group = map.get(date) ?? [];
    group.push(session);
    map.set(date, group);
  }

  return map;
}

/** 收集所有会话涉及的唯一文件列表 */
function uniqueFiles(sessions: AgentSession[]): string[] {
  const set = new Set<string>();
  for (const s of sessions) {
    for (const f of s.affectedFiles) {
      if (f) set.add(f);
    }
  }
  return [...set].sort();
}

/** 构建日期范围描述标签 */
function buildDateRangeLabel(
  startDate: string | undefined,
  endDate: string | undefined,
  language: ExportLanguage,
): string {
  if (!startDate && !endDate) return '';
  if (startDate && endDate) {
    return language === 'zh'
      ? `${startDate} ~ ${endDate}`
      : `${startDate} to ${endDate}`;
  }
  if (startDate) {
    return language === 'zh' ? `${startDate} 起` : `from ${startDate}`;
  }
  return language === 'zh' ? `截至 ${endDate}` : `until ${endDate}`;
}

/** 渲染单条 Session 的详情 Block（供周报使用） */
function renderSessionBlock(
  session: AgentSession,
  index: number,
  t: (typeof i18n)[ExportLanguage],
): string[] {
  const lines: string[] = [];
  const shortId = session.id.slice(0, 8);

  lines.push(`${t.sessionIndex} #${index} <sub>\`${shortId}\`</sub>`);
  lines.push('');

  // 元数据表格
  lines.push(
    `| ${t.time} | ${t.model} | ${t.source} | ${t.commit} |`,
  );
  lines.push(`|:---|:---|:---|:---|`);
  lines.push(
    `| ${formatTime(session.createdAt)} | \`${session.model}\` | \`${session.source}\` | ${session.commitHash ? `\`${session.commitHash.slice(0, 8)}\`` : t.none} |`,
  );
  lines.push('');

  // Prompt
  lines.push(`${t.prompt}：`);
  lines.push('');
  lines.push(`> ${truncate(session.prompt, 300).replace(/\n/g, '\n> ')}`);
  lines.push('');

  // Reasoning（若有）
  if (session.reasoning) {
    lines.push(`${t.reasoning}：`);
    lines.push('');
    lines.push('<details>');
    lines.push('<summary>展开查看推理过程</summary>');
    lines.push('');
    lines.push(truncate(session.reasoning, 800));
    lines.push('');
    lines.push('</details>');
    lines.push('');
  }

  // Response
  lines.push(`${t.response}：`);
  lines.push('');
  lines.push(`> ${truncate(session.response, 300).replace(/\n/g, '\n> ')}`);
  lines.push('');

  // 涉及文件
  if (session.affectedFiles.length > 0) {
    lines.push(
      `${t.affectedFiles}：${session.affectedFiles.map((f) => `\`${f}\``).join(' · ')}`,
    );
    lines.push('');
  }

  // 标签
  if (session.tags && session.tags.length > 0) {
    lines.push(
      `${t.tags}：${session.tags.map((tag) => `\`${tag}\``).join(' ')}`,
    );
    lines.push('');
  }

  // 备注
  if (session.note) {
    lines.push(`${t.note}：${session.note}`);
    lines.push('');
  }

  return lines;
}

/** CSV 字段转义（包裹双引号，内部双引号加倍） */
function csvEscape(value: string): string {
  const escaped = value.replace(/"/g, '""').replace(/\n/g, ' ');
  return `"${escaped}"`;
}

