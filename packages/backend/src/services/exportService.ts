/** 根据导出格式获取推荐文件名 */
export function suggestFilename(
  format: ExportFormat,
  language: ExportLanguage = 'zh',
): string {
  const fmt = EXPORT_FORMATS.find((f) => f.value === format);
  const ext = fmt?.extension ?? 'txt';
  const now = new Date();
  const dateStr = `${now.getFullYear()}${String(now.getMonth() + 1).padStart(2, '0')}${String(now.getDate()).padStart(2, '0')}`;
  const label = language === 'zh'
    ? { 'weekly-report': '周报', 'pr-description': 'PR说明', jsonl: '原始数据', csv: '数据表格' }[format]
    : format;
  return `agentlog_${label}_${dateStr}.${ext}`;
}
