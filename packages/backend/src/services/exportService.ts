const CSV_HEADERS = [
  'id',
  'createdAt',
  'provider',
  'model',
  'source',
  'workspacePath',
  'prompt',
  'hasReasoning',
  'responseLength',
  'commitHash',
  'affectedFiles',
  'durationMs',
  'tags',
  'note',
] as const;

function renderCsv(sessions: AgentSession[]): string {
  const rows: string[] = [CSV_HEADERS.join(',')];

  for (const s of sessions) {
    const row = [
      csvEscape(s.id),
      csvEscape(s.createdAt),
      csvEscape(s.provider),
      csvEscape(s.model),
      csvEscape(s.source),
      csvEscape(s.workspacePath),
      csvEscape(truncate(s.prompt, 500)),
      String(Boolean(s.reasoning)),
      String(s.response.length),
      csvEscape(s.commitHash ?? ''),
      csvEscape(s.affectedFiles.join(';')),
      String(s.durationMs),
      csvEscape((s.tags ?? []).join(';')),
      csvEscape(s.note ?? ''),
    ];
    rows.push(row.join(','));
  }

  return rows.join('\n');
}

