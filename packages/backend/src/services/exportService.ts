/**
 * 根据 ExportOptions 生成对应格式的导出内容。
 */
export async function exportSessions(
  options: ExportOptions,
): Promise<ExportResult> {
  const {
    format,
    startDate,
    endDate,
    language = 'zh',
    tags,
    workspacePath,
    onlyBoundToCommit,
  } = options;

  // 构建查询过滤器（不分页，全量导出）
  const filter: SessionQueryFilter = {
    startDate,
    endDate,
    tags,
    workspacePath,
    onlyBoundToCommit,
    page: 1,
    pageSize: 99999,
  };

  const { data: sessions, total } = querySessions(filter);
  const generatedAt = new Date().toISOString();

  let content: string;

  switch (format) {
    case 'weekly-report':
      content = renderWeeklyReport(sessions, options, language);
      break;
    case 'pr-description':
      content = renderPrDescription(sessions, options, language);
      break;
    case 'jsonl':
      content = exportSessionsAsJsonl(filter);
      break;
    case 'csv':
      content = renderCsv(sessions);
      break;
    default:
      throw new Error(`[exportService] 不支持的导出格式：${format as string}`);
  }

  return {
    format,
    content,
    sessionCount: total,
    generatedAt,
  };
}

