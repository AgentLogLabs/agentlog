function renderWeeklyReport(
  sessions: AgentSession[],
  options: ExportOptions,
  language: ExportLanguage,
): string {
  const t = i18n[language];
  const lines: string[] = [];

  // ── 标题 ──
  const dateRange = buildDateRangeLabel(options.startDate, options.endDate, language);
  lines.push(`${t.weeklyTitle}${dateRange ? `（${dateRange}）` : ''}`);
  lines.push('');

  if (sessions.length === 0) {
    lines.push(t.noData);
    lines.push('');
    lines.push(t.generatedBy);
    return lines.join('\n');
  }

  // ── 概览 ──
  const boundCount = sessions.filter((s) => s.commitHash).length;
  const allFiles = uniqueFiles(sessions);
  const modelSet = [...new Set(sessions.map((s) => `\`${s.model}\``))];

  lines.push(t.summaryHeading);
  lines.push('');
  lines.push(`| ${t.totalSessions} | ${t.boundSessions} | ${t.unboundSessions} | ${t.filesChanged} |`);
  lines.push(`|:---:|:---:|:---:|:---:|`);
  lines.push(
    `| ${sessions.length} | ${boundCount} | ${sessions.length - boundCount} | ${allFiles.length} |`,
  );
  lines.push('');
  lines.push(`${t.modelsUsed}：${modelSet.join('、') || t.none}`);
  lines.push('');

  // ── 按日期分组的交互详情 ──
  lines.push(t.detailHeading);
  lines.push('');

  const grouped = groupSessionsByDate(sessions);
  let globalIndex = 1;

  for (const [date, daySessions] of grouped) {
    lines.push(`#### 📅 ${date}`);
    lines.push('');

    for (const session of daySessions) {
      lines.push(...renderSessionBlock(session, globalIndex, t));
      lines.push('');
      globalIndex++;
    }
  }

  lines.push('---');
  lines.push('');
  lines.push(t.generatedBy);

  return lines.join('\n');
}

